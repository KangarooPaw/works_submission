//=============================================================================
//インクルードファイル
//=============================================================================
#include "title.h"
#include "scene2d.h"
#include "manager.h"
#include "renderer.h"

//=============================================================================
//静的メンバ変数の初期化
//=============================================================================
LPDIRECT3DTEXTURE9 CTitle::m_pTexture = NULL;

//=============================================================================
//コンストラクタ
//=============================================================================
CTitle::CTitle()
{
	m_pScene2D = NULL;
}

//=============================================================================
//デストラクタ
//=============================================================================
CTitle::~CTitle()
{
}

//=============================================================================
//テクスチャの読み込み
//=============================================================================
HRESULT CTitle::Load(void)
{
	LPDIRECT3DDEVICE9 pDevice;
	pDevice = CManager::GetRenderer()->GetDevice();

	// テクスチャの生成
	D3DXCreateTextureFromFile(pDevice,			// デバイスへのポインタ
		TEXTURE_TITLE,								// ファイルの名前
		&m_pTexture);
	return S_OK;
}

//=============================================================================
//テクスチャの破棄
//=============================================================================
void CTitle::Unload(void)
{
	if (m_pTexture != NULL)
	{
		m_pTexture->Release();
		m_pTexture = NULL;
	}
}

//=============================================================================
//生成処理
//=============================================================================
CTitle * CTitle::Create(float nPosX, float nPosY)
{
	CTitle *pTitle;
	pTitle = new CTitle;
	pTitle->Init(nPosX, nPosY, BG_WIDTH, BG_HEIGHT);
	return pTitle;
}

//=============================================================================
//初期化処理
//=============================================================================
HRESULT CTitle::Init(float nPosX, float nPosY, int nPolygonWidth, int nPolygonHeight)
{
	m_pScene2D = new CScene2D;
	m_pScene2D->Init(nPosX, nPosY, nPolygonWidth, nPolygonHeight);
	m_pScene2D->BindTexture(m_pTexture);

	return S_OK;
}

//=============================================================================
//終了処理
//=============================================================================
void CTitle::Uninit(void)
{
	if (m_pScene2D != NULL)
	{	
		m_pScene2D->Uninit();
		m_pScene2D = NULL;
	}
}

//=============================================================================
//更新処理
//=============================================================================
void CTitle::Update(void)
{
}

//=============================================================================
//描画処理
//=============================================================================
void CTitle::Draw(void)
{
	m_pScene2D->Draw();
}
